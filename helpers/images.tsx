export function shuffleArray(array: StaticImageData[]) {
  for (let i = array.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [array[i], array[j]] = [array[j], array[i]];
  }

  return array;
}

function getCategories(array:StaticImageData[],category:string): CategoryImage[]{
 return  array.map((image:StaticImageData,idx:number) => {
    return {
      id: category+idx,
      src: image,
      category
    }
  })
}

